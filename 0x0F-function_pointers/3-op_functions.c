#include "3-calc.h"
/**
 * op_add - calculates sum of tow numbers
 * @a: an integer
 * @b: an integer
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calculates difference of tow numbers
 * @a: an integer
 * @b: an integer
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calculates product of tow numbers
 * @a: an integer
 * @b: an integer
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calculates the result of the division of tow numbers
 * @a: an integer
 * @b: an integer
 * Return: the the result of the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates the remainder of the division of tow numbers
 * @a: an integer
 * @b: an integer
 * Return: the the remainder of the division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
